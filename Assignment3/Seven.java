/*  Accept range from user and print all numbers between that range*/

public class Seven {

	    public static void main(String[] args) {

	        int low = 10, high = 17;

	        while (low <= high) {
	            boolean flag = false;

	            if (!flag)
	                System.out.print(low + " ");

	            ++low;
	        }
	    }
	}

